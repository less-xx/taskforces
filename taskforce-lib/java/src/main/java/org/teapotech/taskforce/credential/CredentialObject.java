package org.teapotech.taskforce.credential;

public abstract class CredentialObject {

	protected String id;

	protected String name;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
