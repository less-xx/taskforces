package org.teapotech.taskforce.util;

public class ObjectValueExtractException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -1356534153967455944L;

	public ObjectValueExtractException() {
	}

	public ObjectValueExtractException(String message) {
		super(message);
	}

	public ObjectValueExtractException(Throwable cause) {
		super(cause);
	}

	public ObjectValueExtractException(String message, Throwable cause) {
		super(message, cause);
	}

	public ObjectValueExtractException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
